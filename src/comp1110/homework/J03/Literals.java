package comp1110.homework.J03;

public class Literals {
    public static void main(String[] args) {
        float f = 3.14f;
        System.out.println(f);
        int i = 0xaff;
        System.out.println(i);
        boolean b = false;
        System.out.println(b);
        char c = 65;
        System.out.println(c);
        long l = 65;
        System.out.println(l);
    }
}
